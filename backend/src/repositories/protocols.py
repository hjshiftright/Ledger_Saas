from typing import Protocol, Optional, Any, List

class SettingsRepository(Protocol):
    async def get(self, key: str) -> Optional[str]: ...
    async def set(self, key: str, value: str) -> None: ...
    async def get_bulk(self, prefix: str) -> dict[str, str]: ...
    async def delete(self, key: str) -> None: ...
    async def delete_bulk(self, prefix: str) -> None: ...
    async def exists(self, key: str) -> bool: ...

class AccountRepository(Protocol):
    async def create(self, account_data: dict) -> Any: ...
    async def get(self, account_id: int) -> Optional[Any]: ...
    async def get_tree(self) -> List[Any]: ...
    async def update(self, account_id: int, updates: dict) -> Any: ...
    async def has_transactions(self, account_id: int) -> bool: ...
    async def get_children(self, account_id: int) -> List[Any]: ...

class InstitutionRepository(Protocol):
    async def create(self, institution_data: dict) -> Any: ...
    async def get(self, institution_id: int) -> Optional[Any]: ...
    async def list(self) -> List[Any]: ...
    async def update(self, institution_id: int, updates: dict) -> Any: ...

class AccountDetailRepository(Protocol):
    async def create_detail(self, detail_type: str, data: dict) -> Any: ...

class TransactionRepository(Protocol):
    async def create_transaction(self, tx_data: dict, lines_data: List[dict]) -> Any: ...
    async def get_opening_balance_for_account(self, account_id: int) -> Optional[Any]: ...
    async def void_transaction(self, tx_id: int) -> None: ...

class SnapshotRepository(Protocol):
    async def save_net_worth(self, data: dict) -> Any: ...

class ProfileRepository(Protocol):
    async def create(self, profile_data: dict) -> Any: ...
    async def get(self, profile_id: int) -> Optional[Any]: ...
    async def get_by_name(self, name: str) -> Optional[Any]: ...
    async def list(self, limit: int = 10, offset: int = 0, sort_by: str = "id", sort_desc: bool = False, filters: dict = None) -> List[Any]: ...
    async def update(self, profile_id: int, updates: dict) -> Optional[Any]: ...
    async def delete(self, profile_id: int) -> bool: ...
    async def count(self, filters: dict = None) -> int: ...

class GoalRepository(Protocol):
    async def create(self, goal_data: dict) -> Any: ...
    async def get(self, goal_id: int) -> Optional[Any]: ...
    async def update(self, goal_id: int, updates: dict) -> Optional[Any]: ...
    async def list(self) -> List[Any]: ...
    async def delete_all(self) -> None: ...
